package com.atjl.validate.domain.constants;


public class ValueMsg {
    private Object rawVal;
    private String errmsg;

    public Object getRawVal() {
        return rawVal;
    }

    public void setRawVal(Object rawVal) {
        this.rawVal = rawVal;
    }

    public String getErrmsg() {
        return errmsg;
    }

    public void setErrmsg(String errmsg) {
        this.errmsg = errmsg;
    }
}
